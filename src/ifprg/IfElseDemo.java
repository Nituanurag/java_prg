package ifprg;

public class IfElseDemo {

	public static void main(String[] args) {
		int a = 40;
		int b = 20;
		if (a<b)
		{
			System.out.println("a = : " +a);
			System.out.println("b = : " +b);
			System.out.println("a is a smaller number");
		}
			else
			{
				System.out.println("a = : " +a);
				System.out.println("b = : " +b);
				System.out.println("b is a smaller number");
			}
	}
}
